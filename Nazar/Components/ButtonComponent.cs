namespace Nazar.Components;

using System;

/// <summary>
/// Represents a button component with a label and a pressed state.
/// </summary>
public struct ButtonComponent
{
    /// <summary>
    /// The text displayed on the button.
    /// </summary>
    public string Label;

    /// <summary>
    /// Indicates whether the button is currently pressed.
    /// </summary>
    public bool IsPressed;
}
